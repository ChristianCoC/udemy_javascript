// Este estilo de declaracion variables ya esta obsoleto
var miNumero = 10;
console.log(miNumero);

// Este es el actual estilo de declaracion de variables
let miNumero2 = 20;
console.log(miNumero2);

// Reglas para declarar variables
/*  Debe comenzar con una letra, un guion bajo o el simbolo $.
    Los siguientes caracteres pueden ser:
    letras, digitos, guiones bajos o $ 
*/

// Ejemplos
let nombre = "David"; 
console.log(nombre);

let nombre2 = "David";
console.log(nombre2);

// CamelCase
let miNombre = "David";
console.log(miNombre);
 
let _miNombre = "David";
console.log(_miNombre);

let $miNombre = "David";
console.log($miNombre);
