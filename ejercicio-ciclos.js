// Imprimir los primeros 10 números de 3 en 3
for (let i = 1; i <= 10; i++) {
    console.log(i * 3);
}

// Imprimir los numeros desde 1 sumados de a 3 hasta 10
for (let i = 1; i <= 10; i += 3) {
    console.log(i);
}

// Imprimir los numeros desde 1 sumados de a 3 hasta -10
for (let i = 1; i >= -10; i -= 3) {
    console.log(i);
}