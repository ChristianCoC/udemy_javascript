// Number -> Almacena valores numericos
let numero = 10;
console.log(numero);
let flotante = 10.5;
console.log(flotante);

// String -> Almacena cadenas de caracteres
let texto = "Hola";
console.log(texto);
let textoDos = 'Hola';
console.log(textoDos);
let textoTres = `Hola`;
console.log(textoTres);

// Boolean -> Almacena valores logicos
let verdadero = true;
let falso = false;
console.log(verdadero);
console.log(falso);

// Null -> Ausencia de la referencia de un valor
let nulo = null;
console.log(nulo);

// Undefined -> Ausencia de valor
let indefinido;
console.log(indefinido);

// Consultar el tipo de dato de una variable
console.log(typeof numero);
console.log(typeof flotante);
console.log(typeof texto);
console.log(typeof verdadero);
console.log(typeof falso);
console.log(typeof nulo);
console.log(typeof indefinido);
