// Precedencia de operadores

/*
    1. Parentesis y/o corchetes
    2. Operadores unarios (++, --, !)
    3. Multiplicacion, division y modulo
    4. Suma y resta
    5. Operadores de comparacion relacionales ( >, <, >=, <= )
    6. Operadores de comparacion igualdad ( ==, !=, ===, !== )
    7. Operadores logicos (&&, ||, !)
    8. Operadores de asignacion ( =, +=, -=, *=, /=, %=, **=, &=, |=, ^=, <<=, >>=, >>>= )
*/

// Ejemplos
console.log(10 + 5 * 3); // 25
console.log((10 + 5) * 3); // 45

console.log(10 / 5 + 3 * 2 - 1); // 7